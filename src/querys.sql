	LAST_NOTIFIED_PRICE, MIN_PRICE, MAX_PRICE
FROM
	WISHLIST_GAME WG, LIST LI, STORE SH
WHERE
	LI.ID = WG.ID_LIST AND
	SH.ID = WG.ID_STORE AND
	LI.ID_USER = 1 AND
	WG.URL = "URL";
    
